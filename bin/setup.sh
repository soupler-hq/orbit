#!/usr/bin/env bash
# bin/setup.sh — Orbit unified installer
# Auto-detects installed coding agents and sets up Orbit for each.
# Usage:  npm run setup
#         npm run setup -- --tool claude
#         npm run setup -- --tool claude,codex
set -euo pipefail

FRAMEWORK_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
ORBIT_DIR="$FRAMEWORK_DIR/.orbit"
ADAPTERS_DIR="$ORBIT_DIR/adapters"

# ── Colour helpers ─────────────────────────────────────────────────────────────
G='\033[0;32m'
Y='\033[1;33m'
R='\033[0;31m'
B='\033[1m'
N='\033[0m'

# ── Tool detection ─────────────────────────────────────────────────────────────

detect_tools() {
  local tools=()
  command -v claude      &>/dev/null && tools+=("claude")
  command -v codex       &>/dev/null && tools+=("codex")
  [[ -d "$HOME/.antigravity" ]] && tools+=("antigravity")
  echo "${tools[*]:-}"
}

# ── Adapter directory setup ────────────────────────────────────────────────────

create_adapter_dir() {
  local tool="$1"
  local adapter_path="$ADAPTERS_DIR/$tool"
  mkdir -p "$adapter_path"

  local config_ref
  case "$tool" in
    claude)      config_ref=".claude/CLAUDE.md (generated by install.sh)" ;;
    codex)       config_ref=".codex/INSTRUCTIONS.md (generated by install.sh)" ;;
    antigravity) config_ref=".antigravity/CLAUDE.md (generated by install.sh)" ;;
    *)           config_ref="(unknown config path)" ;;
  esac

  local hook_support
  case "$tool" in
    claude) hook_support="✅" ;;
    *)      hook_support="❌" ;;
  esac

  # Use single-quoted EOF to prevent shell expansion in the heredoc
  cat > "$adapter_path/README.md" << 'HEREDOC_END'
# Orbit Adapter

This directory marks the Orbit adapter for this tool.

Generated config files are created by `install.sh`. Do not hand-edit.
Re-run `npm run setup` to regenerate.
HEREDOC_END

  # Append dynamic content separately
  {
    echo ""
    echo "## Tool: ${tool}"
    echo "Config: \`${config_ref}\`"
    echo ""
    echo "## Hook support"
    echo ""
    echo "| Hook        | Support |"
    echo "|-------------|---------|"
    echo "| PreToolUse  | ${hook_support} |"
    echo "| PostToolUse | ${hook_support} |"
    echo "| PreCompact  | ${hook_support} |"
    echo "| Stop        | ${hook_support} |"
  } >> "$adapter_path/README.md"

  echo "$adapter_path"
}

# ── Install one tool ────────────────────────────────────────────────────────────

install_tool() {
  local tool="$1"
  case "$tool" in
    claude)
      bash "$FRAMEWORK_DIR/install.sh" --local --tool claude
      ;;
    codex)
      bash "$FRAMEWORK_DIR/install.sh" --local --tool codex
      ;;
    antigravity)
      # Antigravity uses the same instruction format as Claude
      bash "$FRAMEWORK_DIR/install.sh" --local --tool claude
      echo "  ℹ  Antigravity: using Claude-compatible adapter (best-effort until API stabilises)"
      ;;
    *)
      echo "  Unknown tool: $tool — skipping"
      return 1
      ;;
  esac
}

# ── Main ───────────────────────────────────────────────────────────────────────

main() {
  local explicit_tools=""

  # Parse flags
  while [[ $# -gt 0 ]]; do
    case "$1" in
      --tool)
        explicit_tools="$2"
        shift 2
        ;;
      --tool=*)
        explicit_tools="${1#--tool=}"
        shift
        ;;
      -h|--help)
        echo "Usage: npm run setup [-- --tool <tool1,tool2>]"
        echo "  --tool   Comma-separated list of tools (claude, codex, antigravity)"
        echo "           If omitted, auto-detects installed tools"
        exit 0
        ;;
      *)
        echo "Unknown flag: $1" >&2
        echo "Usage: npm run setup [-- --tool claude,codex]" >&2
        exit 1
        ;;
    esac
  done

  echo ""
  echo -e "${B}━━━ Orbit Setup ━━━━━━━━━━━━━━━━━━━━━━━━${N}"
  echo ""

  # Determine tools to install
  local tools_str
  if [[ -n "$explicit_tools" ]]; then
    tools_str="${explicit_tools//,/ }"
  else
    tools_str="$(detect_tools)"
  fi

  # Convert to array safely
  local tools=()
  if [[ -n "$tools_str" ]]; then
    IFS=' ' read -ra tools <<< "$tools_str"
  fi

  if [[ ${#tools[@]} -eq 0 ]]; then
    echo -e "  ${Y}⚠️  No coding agents detected.${N}"
    echo "     Install Claude Code, Codex, or Antigravity first."
    echo "     Then re-run: npm run setup"
    echo ""
    exit 0
  fi

  # Track results using temp files
  local tmp_dir
  tmp_dir="$(mktemp -d)"
  # shellcheck disable=SC2064
  trap "rm -rf '$tmp_dir'" EXIT

  for tool in "${tools[@]}"; do
    echo -e "${B}Installing for ${tool}...${N}"
    # Run each tool install in a subshell so one failure doesn't abort others
    if (install_tool "$tool") 2>&1; then
      local adapter_abs
      adapter_abs="$(create_adapter_dir "$tool")"
      local adapter_rel="${adapter_abs#$FRAMEWORK_DIR/}"
      echo "$adapter_rel" > "$tmp_dir/${tool}.ok"
    else
      echo "failed" > "$tmp_dir/${tool}.fail"
      echo -e "  ${R}✗ install failed for ${tool} — continuing with remaining tools${N}"
    fi
    echo ""
  done

  # Print summary table
  echo -e "  ${B}Tool         Detected   Installed   Adapter${N}"
  echo "  ─────────────────────────────────────────────────────────"

  for tool in claude codex antigravity; do
    local detected="✗"
    local installed="—"
    local adapter_col="—"

    for t in "${tools[@]}"; do
      if [[ "$t" == "$tool" ]]; then
        detected="✓"
        if [[ -f "$tmp_dir/${tool}.ok" ]]; then
          installed="✓"
          adapter_col="$(cat "$tmp_dir/${tool}.ok")"
        fi
        break
      fi
    done

    printf "  %-12s %-10s %-11s %s\n" "$tool" "$detected" "$installed" "$adapter_col"
  done

  echo ""

  # ── Bootstrap context.db ────────────────────────────────────────────────────
  # Always run after install so context.db reflects the current repo state.
  # --force overwrites any existing db so setup is always idempotent.
  echo -e "${B}Bootstrapping context.db...${N}"
  if node "$FRAMEWORK_DIR/bin/bootstrap.js" --force 2>&1; then
    echo -e "  ${G}✓ context.db ready at .orbit/context.db${N}"
  else
    echo -e "  ${Y}⚠  bootstrap failed — context.db may be missing or stale${N}"
    echo "     Run manually: npm run bootstrap"
  fi

  echo ""
  echo -e "${B}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${N}"
  echo -e "  ${G}Setup complete.${N}"
  echo ""
  echo -e "  ${B}What to do next:${N}"
  echo ""

  # Tool-specific next steps
  for tool in "${tools[@]}"; do
    case "$tool" in
      claude)
        echo -e "  ${G}Claude Code (VS Code):${N}"
        echo "    Open this folder in VS Code — Orbit context loads automatically."
        echo "    First command: /orbit:resume"
        echo ""
        ;;
      codex)
        echo -e "  ${G}Codex:${N}"
        echo "    Run:  codex"
        echo "    Then: Read .codex/policy.md and .codex/INSTRUCTIONS.md to load Orbit context."
        echo "          Read .orbit/state/STATE.md for project state."
        echo "          Then: /orbit:resume"
        echo ""
        ;;
      antigravity)
        echo -e "  ${G}Antigravity:${N}"
        echo "    Open this folder — read .antigravity/CLAUDE.md at session start."
        echo "    First command: /orbit:resume"
        echo ""
        ;;
    esac
  done

  echo -e "  ${B}Verify context:${N}"
  echo "    node bin/context.js --load minimal"
  echo ""
  echo -e "${B}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${N}"
  echo ""
}

main "$@"
